// Incriment operator
// ++i (Pre Increment)
// i++ (Post Increment)
#include <stdio.h>
int main()
{
    int i = 1;
    printf("%d \n", i++); // use then increase
    printf("Incremient to %d", i);

    printf("%d \n", ++i); // increase then use
    printf("Incremient to %d", i);
    return 0;
}